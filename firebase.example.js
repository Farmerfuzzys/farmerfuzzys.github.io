// Firebase Configuration Template
// ================================
// 
// To use this application with your own Firebase project:
// 
// 1. Create a new Firebase project at https://console.firebase.google.com
// 
// 2. Enable authentication methods:
//    - Go to "Build" > "Authentication"
//    - Enable "Email/Password" authentication
// 
// 3. Create a Firestore database:
//    - Go to "Build" > "Firestore Database"
//    - Start in "Test mode" initially (update rules for production)
//
// 4. Get your configuration:
//    - Go to "Project Settings" (gear icon)
//    - Scroll down to find the Web SDK snippet
//    - Copy the config object
//
// 5. Create a file named "firebase-config.js" in the root directory
//    with the following structure (do NOT commit this file):
//
// ===== START: firebase-config.js =====
// 
// export const firebaseConfig = {
//   apiKey: "YOUR_API_KEY_HERE",
//   authDomain: "your-project.firebaseapp.com",
//   projectId: "your-project-id",
//   storageBucket: "your-project.appspot.com",
//   messagingSenderId: "YOUR_SENDER_ID",
//   appId: "1:YOUR_APP_ID:web:YOUR_WEB_ID"
// };
//
// ===== END: firebase-config.js =====
//
// The firebase.js file will automatically load your configuration
// from this file. Make sure firebase-config.js is listed in .gitignore
// to prevent committing sensitive credentials.
//
// For more information, visit: https://firebase.google.com/docs/web/setup
